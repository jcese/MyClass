package com.example.demo;

public class People {
	private int id ;
	private String name ;
	private String salcry ;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSalcry() {
		return salcry;
	}
	public void setSalcry(String salcry) {
		this.salcry = salcry;
	}
}
